  return undef if length($s.$e) < 3;
  while (length($s)<3) {
    $s .= substr($e,0,1);
    $e = substr($e,1);
  }   
  while (1) {
    my @tmp = reduce1($v,$s,$e);
    if ($#tmp) {
      my $tmp = join ':', @tmp;
