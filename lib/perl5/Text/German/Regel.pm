$debug = 0;
@REGEL = ();                    # -w

{
  local ($_);
  
  while (<DATA>) {
    chomp;
    my ($regel, $a,$b,$c,$d,$e,@f) = split(/:/, $_);
    next unless $regel;
    push(@{$REGEL[$regel]}, [$a,
                             $b,
                             $c,
                             bit_to_int($d),
                             bit_to_int($e),
                             @f]);
  }
  close DATA;
}
